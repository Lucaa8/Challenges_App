        //Récupère la latence entre le client et le serveur ciblé en paramètre
        public static long getPing(String ip)
        {
            Ping p = new Ping();
            PingReply r;
            try
            {
                r = p.Send(ip, 500);
                if (r.Status == IPStatus.Success)
                {
                    return r.RoundtripTime;
                }
            }
            catch (Exception) { }
            return -1L;
        }
        //Récupère l'ip interne d'un utilisateur (plus utilisé -> remplacé par 127.0.0.1)
        public static String? getInternIP()
        {
            foreach (IPAddress ip in Dns.GetHostEntry(Dns.GetHostName()).AddressList)
            {
                if (ip.AddressFamily == AddressFamily.InterNetwork)
                {
                    return ip.ToString();
                }
            }
            return null;
        }
        //Récupère l'ip externe d'un utilisateur
        public static String getExternIP()
        {
            //Prépare une connexion au site distant
            Uri url = new Uri("https://ipecho.net/plain");
            //Demande la connexion au site distant
            WebRequest request = WebRequest.Create(url);
            //Essaye de récupèrer une réponse (peut être null si le site est down je suppose)
            WebResponse response = request.GetResponse();
            //Permet de lire le contenu du site (Ce site contient uniquement l'ip, pas besoin de gerer le string)
            StreamReader sr = new StreamReader(response.GetResponseStream());
            String ip = sr.ReadToEnd();
            //Ferme le Reader
            sr.Close();
            return ip;
        }
        public static String getTimeNow()
        {
            DateTime now = DateTime.UtcNow;
            now = now.AddMilliseconds(TimeZoneInfo.Local.GetUtcOffset(now).TotalMilliseconds);
            return now.ToString("HH:mm:ss");
        }
